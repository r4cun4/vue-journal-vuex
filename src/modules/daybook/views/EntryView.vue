import { defineAsyncComponent } from 'vue';
<template>
    <template v-if="entry">
        <div class="entry-title d-flex justify-content-between p-2">
            <div>
                <span class="text-success fs-3 fw-bold">{{ day }}</span>
                <span class="mx-1 fs-3">{{ month }}</span>
                <span class="mx-1 fs-3 fw-light">{{ yearDay }}</span>
            </div>

            <div>
                <button class="btn btn-danger mx-2">
                    Borrar
                    <i class="fa fa-trash alt"></i>
                </button>

                <button class="btn btn-primary">
                    Borrar
                    <i class="fa fa-upload"></i>
                </button>
            </div>
        </div>

        <hr>
        <div class="d-flex flex-column px-3 h-75">
            <textarea
                v-model="entry.text"
                placeholder="¿Qué sucedió hoy?"
            ></textarea>
        </div>

        <img
            src="https://www.imagenesbonitasname.com/covers/thumbs/gatito-imagen-tierna.jpg"
            alt="entry-picture"
            class="img-thumbnail">

    </template>

    <TheFab
        icon="fa-save"
    />
</template>

<script>
import { defineAsyncComponent } from 'vue'
import { mapGetters } from 'vuex'; // computed

import getDayMonthYear from '../components/helpers/getDayMonthYear'

export default {
    props: {
        id: {
            type: String,
            required: true
        }
    },
    components: {
        TheFab: defineAsyncComponent(() => import('../components/TheFab.vue'))
    },

    data() {
        return {
            entry: null
        }
    },

    computed: {
        ...mapGetters('journalModule', ['getEntriesById']),
        day() {
            const { day } = getDayMonthYear( this.entry.date )
            return day
        },
        month() {
            const { month } = getDayMonthYear( this.entry.date )
            return month
        },
        yearDay() {
            const { yearDay } = getDayMonthYear( this.entry.date )
            return yearDay
        }
    },

    methods: {
        loadEntry() {
            const entry = this.getEntriesById( this.id )
            if ( !entry ) return this.$router.push({ name: 'no-entry' })

            this.entry = entry
        }
    },

    created() {
        this.loadEntry()
    },

    watch: {
        id() {
            this.loadEntry()
        }
    }
}
</script>

